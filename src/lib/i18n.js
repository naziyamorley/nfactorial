export const LangContext = createContext(null)

export function useLang() {
  return useContext(LangContext)
}

export function getLangFromStorage() {
  return localStorage.getItem('chessy_lang') || 'ru'
}

export function LangProvider({ children }) {
  const [lang, setLangState] = useState(getLangFromStorage)

  function setLang(l) {
    localStorage.setItem('chessy_lang', l)
    setLangState(l)
  }

  function t(key) {
    return T[lang]?.[key] ?? T.ru?.[key] ?? key
  }

  return createElement(LangContext.Provider, { value: { lang, setLang, t } }, children)
}

// ─── Theme (light / dark) ─────────────────────────────────────────────────────

export const ThemeContext = createContext(null)

export function useTheme() {
  return useContext(ThemeContext)
}

function getThemeFromStorage() {
  return localStorage.getItem('chessy_theme') || 'light'
}

export function ThemeProvider({ children }) {
  const [theme, setThemeState] = useState(getThemeFromStorage)

  useEffect(() => {
    document.documentElement.setAttribute('data-theme', theme)
  }, [theme])

  function setTheme(t) {
    localStorage.setItem('chessy_theme', t)
    setThemeState(t)
  }

  function toggleTheme() {
    setTheme(theme === 'dark' ? 'light' : 'dark')
  }

  return createElement(ThemeContext.Provider, { value: { theme, setTheme, toggleTheme } }, children)
}
